export const getBranchColor = (branch) => {
  switch (branch) {
    case "USAF": return "text-blue-400";
    case "US Navy": return "text-cyan-400";
    case "RAF": return "text-red-400";
    case "USMC": return "text-orange-400";
    default: return "text-muted-foreground";
  }
};

export const getStatusColor = (status) => {
  switch (status) {
    case "en-route": return "bg-emerald-500/20 text-emerald-400 border-emerald-500/30";
    case "scheduled": return "bg-amber-500/20 text-amber-400 border-amber-500/30";
    case "landed": return "bg-blue-500/20 text-blue-400 border-blue-500/30";
    case "delayed": return "bg-red-500/20 text-red-400 border-red-500/30";
    default: return "bg-muted text-muted-foreground";
  }
};

